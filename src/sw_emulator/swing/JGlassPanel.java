  /**
   * Apply the option for TMPx
   */
  public void applyOptionGlass() {
    switch (option.glassStarting) {
      case FAKEZ:
        jRadioButtonGlassFakeStarting.setSelected(true);   
        break;
    }
    
    switch (option.glassOrigin) {
      case DOT_ORG:
      case ORG:
        jRadioButtonGlassOrigin.setSelected(true);  
        break;
    }      
    
    switch (option.glassLabel) {
      case NAME:
        jRadioButtonGlassLabelName.setSelected(true);
        break;
      case NAME_COLON:
        jRadioButtonGlassLabelNameColon.setSelected(true);  
        break;  
    }
    
    switch (option.glassComment) {
      case SEMICOLON:
        jRadioButtonGlassSemicolonComment.setSelected(true);
        break;               
    }    
    
    switch (option.glassByte) {
      case DB_BYTE:  
        jRadioButtonGlassDbByte.setSelected(true);
        break;      
    }
    
    switch (option.glassWord) {
      case DOT_WORD:  
        jRadioButtonGlassDwWord.setSelected(true);
        break;          
    } 
    
    switch (option.glassWordSwapped) {
      case MACRO5_WORD_SWAPPED:  
        jRadioButtonGlassMacroWordSwapped.setSelected(true);
        break;          
    }
    
    switch (option.glassTribyte) {
      case MACRO5_TRIBYTE:  
        jRadioButtonGlassMacroTribyte.setSelected(true);
        break;          
    }
    
    switch (option.glassLong) {
      case DD_LONG:  
        jRadioButtonGlassDdLong.setSelected(true);
        break;          
    }
    
    switch (option.glassAddress) {
      case DW_ADDR:  
        jRadioButtonGlassDwAddress.setSelected(true);
        break;          
    }
    
    switch (option.glassStackWord) {
      case MACRO4_STACKWORD:  
        jRadioButtonGlassMacroStackWord.setSelected(true);
        break;          
    }
    
    switch (option.dasmMonoSprite) {
      case BYTE_HEX:
        jRadioButtonGlassByteHexMonoSprite.setSelected(true);
        break;
      case BYTE_BIN:
        jRadioButtonGlassByteBinMonoSprite.setSelected(true);
        break;
      case MACRO5_HEX:
        jRadioButtonGlassMacroHexMonoSprite.setSelected(true);
        break;  
      case MACRO5_BIN:
        jRadioButtonGlassMacroBinMonoSprite.setSelected(true);
        break;  
    }
    
    switch (option.dasmMultiSprite) {
      case BYTE_HEX:
        jRadioButtonGlassByteHexMultiSprite.setSelected(true);
        break;
      case BYTE_BIN:
        jRadioButtonGlassByteBinMultiSprite.setSelected(true);
        break;
      case MACRO5_HEX:
        jRadioButtonGlassMacroHexMultiSprite.setSelected(true);
        break;  
      case MACRO5_BIN:
        jRadioButtonGlassMacroBinMultiSprite.setSelected(true);
        break;  
    }    
    
    switch (option.glassText) {
      case DB_BYTE_TEXT:  
        jRadioButtonGlassDbText.setSelected(true);
        break;          
    } 
    
    switch (option.glassNumText) {
      case DB_BYTE_NUMTEXT:  
        jRadioButtonGlassDbNumText.setSelected(true);
        break;          
    } 
    
    switch (option.glassZeroText) {
      case DB_BYTE_ZEROTEXT:  
        jRadioButtonGlassDbZeroText.setSelected(true);
        break;          
    }
  }
